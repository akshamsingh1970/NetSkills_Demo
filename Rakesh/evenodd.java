public class evenodd
{
	public static void main(String[] args) {
	    System.out.print("Even Number between 1 to 20 is ");
		for(int i=0; i<=20; i=i+2)
		{
		    System.out.print(i+" ");
		}
		System.out.print("\n");
		System.out.print("Even Number between 1 to 20 is ");
		for(int i=1; i<=20; i=i+2)
		{
		    System.out.print(i+" ");
		}
	}
}
