package NetSkills_Demo.vinay;

public class swapNum {
    public static void main(String[] args) {
        // Swapping numbers
        System.out.println("Swapping numbers");
        int a = 5;
        int b = 10;
        System.out.println("Initial values: a=" + a + ", b=" + b);
        int temp = a;
        a = b;
        b = temp;
        System.out.println("Final values: a=" + a + ", b=" + b);
    }
}
