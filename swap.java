class swap {
    public static void main(String[] args) {
        int a, b, t;// x and y are to swap   
       a = 5;  
       b = 10;  
       System.out.println("before swapping numbers: "+a +"  "+ b); 
       System.out.println( );  
       t = a;  
       a = b;  
       b = t;  
       System.out.println("After swapping: "+a +"   " + b);  
       System.out.println( );  
    }
}