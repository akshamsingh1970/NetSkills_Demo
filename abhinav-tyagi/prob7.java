public class prob7 {
  public static void main(String[] args) {

    // Initialize a and b
    int a = 5;
    int b = 10;

    // Swap a and b using a temporary variable
    int temp = a;
    a = b;
    b = temp;

    // Print the values of a and b after swapping
    System.out.println("a = " + a);
    System.out.println("b = " + b);
  }
}
